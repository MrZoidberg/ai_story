import Head from 'next/head';
import * as React from 'react';
import * as utils from '../src/utils';
import styles from '../styles/Home.module.css';
import Layout, { siteTitle } from '../components/layout';
import StoriesGrid from '../components/stories_grid';
import MyAppBar from '../components/app_bar';

function Home(props) {
  return (
    <Layout home>
      <Head>
        <title>{siteTitle}</title>
      </Head>
      <MyAppBar />
      <section className={styles.headerSection}>
        <h1 className={styles.title}>
          Welcome to <a href="">AI.Stories!</a>
        </h1>

        <p className={styles.description}>
          Funny stories generated by GPT-3 and other AI models
        </p>
      </section>
      <StoriesGrid {...props}/>
    </Layout>
  )
}



// This function gets called at build time on server-side.
// It won't be called on client-side, so you can even do
// direct database queries.
export async function getStaticProps({ locale }) {
  
  const language = utils.transformLocale(locale);
  console.log("language: " + language);

  const res = await fetch(`https://lwtmylvikd.execute-api.us-east-1.amazonaws.com/Development/api/stories?language=${language}&pageSize=10`)
  const stories = await res.json()

  // By returning { props: { posts } }, the Blog component
  // will receive `posts` as a prop at build time
  return {
    props: {
      page_data: stories
    },
  }
}

export default Home