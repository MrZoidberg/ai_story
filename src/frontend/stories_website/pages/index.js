import Head from 'next/head';
import * as React from 'react';
import Container from '@mui/material/Container';
import Typography from '@mui/material/Typography';
import styles from '../styles/Home.module.css';

function Home({page_data}) {
  return (
    <Layout home>
      <Head>
        <title>{siteTitle}</title>
      </Head>
      <section className={styles.headingMd}>
        <h1 className={styles.title}>
          Welcome to <a href="">AI.Stories!</a>
        </h1>

        <p className={styles.description}>
          Funny stories generated by GPT-3 and other AI models
        </p>
      </section>
      {
        page_data["Page"]["Stories"].map((story) => {
          return (
            <section key={story.StoryId}>
              <p className={styles.description}>
                {story["Story"]}
              </p>
            </section>
          )
        })
      }
    </Layout>
  )
}

// This function gets called at build time on server-side.
// It won't be called on client-side, so you can even do
// direct database queries.
export async function getStaticProps() {
  // Call an external API endpoint to get posts.
  // You can use any data fetching library
  const res = await fetch('https://lwtmylvikd.execute-api.us-east-1.amazonaws.com/Development/api/stories?language=en&pageSize=10')
  const stories = await res.json()

  // By returning { props: { posts } }, the Blog component
  // will receive `posts` as a prop at build time
  return {
    props: {
      page_data: stories
    },
  }
}

export default Home